(() => {
  const $ = (id) => document.getElementById(id);

  const trModal = $("trModal");
  const bulkbar = $("bulkbar");
  const selectAll = $("selectAll");
  const bulkAction = $("bulkAction");
  const bulkActionVal = $("bulkActionVal");
  const bulkIdsVal = $("bulkIdsVal");
  const bulkForm = $("bulkForm");
  const bulkGeneratePanel = $("bulkGeneratePanel");
  const issueForm = $("issueForm");
  const revokeForm = $("revokeForm");
  const deleteForm = $("deleteForm");

  const trForm = $("trForm");
  const trModalTitle = $("trModalTitle");
  const mStudent = $("mStudent");
  const mClassGroup = $("mClassGroup");
  const mSectionId = $("mSectionId");
  const mStreamId = $("mStreamId");
  const mKind = $("mKind");
  const mRangeMode = $("mRangeMode");
  const mAYFrom = $("mAYFrom");
  const mAYTo = $("mAYTo");
  const mTermFrom = $("mTermFrom");
  const mTermTo = $("mTermTo");
  const mIncludeDraft = $("mIncludeDraft");
  const mAutoGenerated = $("mAutoGenerated");
  const mNotes = $("mNotes");
  const mTeacherComment = $("mTeacherComment");
  const mHeadTeacherComment = $("mHeadTeacherComment");
  const mStatus = $("mStatus");
  const mId = $("mId");

  function open(el) {
    if (!el) return;
    el.classList.add("show");
    document.body.style.overflow = "hidden";
  }

  function close(el) {
    if (!el) return;
    el.classList.remove("show");
    document.body.style.overflow = "";
  }

  function selectedIds() {
    return Array.from(document.querySelectorAll(".selChk"))
      .filter((cb) => cb.checked)
      .map((cb) => cb.dataset.id);
  }

  function syncBulkbar() {
    const ids = selectedIds();
    $("selCount").textContent = String(ids.length);
    bulkbar.classList.toggle("show", ids.length > 0);
  }

  function toggleRangeFields() {
    const mode = String(mRangeMode?.value || "auto");
    const disableManual = mode !== "custom";
    [mAYFrom, mAYTo, mTermFrom, mTermTo].forEach((el) => {
      if (!el) return;
      el.disabled = disableManual;
    });
  }

  function refreshAcademicSelector() {
    window.AcademicSelector?.refresh(document);
  }

  function resetModal() {
    if (!trForm) return;
    trForm.action = "/admin/transcripts";
    mId.value = "";
    mStudent.value = "";
    mClassGroup.value = "";
    if (mSectionId) mSectionId.value = "";
    if (mStreamId) mStreamId.value = "";
    mKind.value = "official";
    mRangeMode.value = "auto";
    mAYFrom.value = "";
    mAYTo.value = "";
    mTermFrom.value = 1;
    mTermTo.value = 3;
    mIncludeDraft.checked = false;
    mAutoGenerated.checked = true;
    mNotes.value = "";
    mTeacherComment.value = "";
    mHeadTeacherComment.value = "";
    mStatus.value = "draft";
    refreshAcademicSelector();
    toggleRangeFields();
  }

  function openCreate() {
    trModalTitle.textContent = "Generate Transcript";
    resetModal();
    open(trModal);
  }

  function openEditFromBtn(btn) {
    const status = btn.dataset.status || "draft";
    if (status === "issued") {
      alert("Issued transcripts cannot be edited. Use Clone instead.");
      return;
    }

    trModalTitle.textContent = "Edit Transcript";
    resetModal();

    const id = btn.dataset.id;
    mId.value = id;
    mStatus.value = status;
    trForm.action = `/admin/transcripts/${encodeURIComponent(id)}`;

    mStudent.value = btn.dataset.student || "";
    mClassGroup.value = btn.dataset.classgroup || "";
    if (mSectionId) mSectionId.value = btn.dataset.sectionid || "";
    if (mStreamId) mStreamId.value = btn.dataset.streamid || "";
    mKind.value = btn.dataset.kind || "official";
    mRangeMode.value = btn.dataset.rangemode || "auto";
    mAYFrom.value = btn.dataset.ayfrom || "";
    mAYTo.value = btn.dataset.ayto || "";
    mTermFrom.value = btn.dataset.termfrom || 1;
    mTermTo.value = btn.dataset.termto || 3;
    mIncludeDraft.checked = btn.dataset.incdraft === "1";
    mAutoGenerated.checked = btn.dataset.autogen !== "0";
    mNotes.value = btn.dataset.notes || "";
    mTeacherComment.value = btn.dataset.teachercomment || "";
    mHeadTeacherComment.value = btn.dataset.headteachercomment || "";

    toggleRangeFields();
    refreshAcademicSelector();
    open(trModal);
  }

  function saveTranscript() {
    if (!mStudent.value) return alert("Learner is required.");
    if (mRangeMode.value === "custom") {
      const tf = Number(mTermFrom.value || 1);
      const tt = Number(mTermTo.value || 3);
      if (tf > tt) return alert("Term From cannot be greater than Term To.");
    }
    trForm.submit();
  }

  function issue(id) {
    if (!window.confirm("Issue this transcript? It will be snapshotted and locked.")) return;
    issueForm.action = `/admin/transcripts/${encodeURIComponent(id)}/issue`;
    issueForm.submit();
  }

  function revoke(id) {
    const reason = (window.prompt("Revoke reason (optional):") || "").trim().slice(0, 300);
    if (!window.confirm("Revoke this transcript?")) return;

    revokeForm.action = `/admin/transcripts/${encodeURIComponent(id)}/revoke`;

    let inp = revokeForm.querySelector('input[name="reason"]');
    if (!inp) {
      inp = document.createElement("input");
      inp.type = "hidden";
      inp.name = "reason";
      revokeForm.appendChild(inp);
    }
    inp.value = reason;
    revokeForm.submit();
  }

  function del(id) {
    if (!window.confirm("Delete this transcript permanently?")) return;
    deleteForm.action = `/admin/transcripts/${encodeURIComponent(id)}/delete`;
    deleteForm.submit();
  }

  function bulkApply() {
    const action = String(bulkAction?.value || "").trim();
    const ids = selectedIds();

    if (!action || !ids.length) {
      alert("Choose a bulk action and select at least one transcript.");
      return;
    }

    if (action === "delete" && !window.confirm(`Delete ${ids.length} transcript(s)?`)) return;
    if (action === "revoke" && !window.confirm(`Revoke ${ids.length} transcript(s)?`)) return;
    if (action === "issue" && !window.confirm(`Issue ${ids.length} transcript(s)?`)) return;
    if (action === "regenerate" && !window.confirm(`Regenerate ${ids.length} draft transcript(s) from latest results?`)) return;

    bulkActionVal.value = action;
    bulkIdsVal.value = ids.join(",");
    bulkForm.submit();
  }

  function toggleBulkGenerate() {
    if (!bulkGeneratePanel) return;
    bulkGeneratePanel.classList.toggle("show");
    refreshAcademicSelector();
  }

  document.addEventListener("click", function (e) {
    const btn = e.target.closest("[data-action]");
    if (!btn) return;

    const action = btn.dataset.action;

    if (action === "openCreate") return openCreate();
    if (action === "toggleBulkGenerate") return toggleBulkGenerate();
    if (action === "closeModal") return close(trModal);
    if (action === "saveTranscript") return saveTranscript();
    if (action === "openEdit") return openEditFromBtn(btn);
    if (action === "issue") return issue(btn.dataset.id);
    if (action === "revoke") return revoke(btn.dataset.id);
    if (action === "delete") return del(btn.dataset.id);
    if (action === "bulkApply") return bulkApply();
  });

  if (selectAll) {
    selectAll.addEventListener("change", function () {
      document.querySelectorAll(".selChk").forEach((cb) => {
        cb.checked = selectAll.checked;
      });
      syncBulkbar();
    });
  }

  document.querySelectorAll(".selChk").forEach((cb) => {
    cb.addEventListener("change", syncBulkbar);
  });

  if (mRangeMode) {
    mRangeMode.addEventListener("change", toggleRangeFields);
    toggleRangeFields();
  }

  if (trModal) {
    trModal.addEventListener("click", function (e) {
      if (e.target === trModal) close(trModal);
    });
  }

  document.addEventListener("keydown", function (e) {
    if (e.key === "Escape") close(trModal);
  });

  syncBulkbar();
})();
